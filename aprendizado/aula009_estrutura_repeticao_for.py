# Aula 009 - Estrutura de repetição for

# Lembrando que "for" é um laço com variável de controle 

# for i in range(1, 10): 

# Contar de 10 vezes (de 0 a 9)
for i in range(0, 10):
    print(i)
print('FIM')

# Contar de 10 até 1 
for i in range(10, 0, -1): # O último parâmetro é referente a operação de iteração
    print(i)

# Contar de 0 a 6 indo de 2 em 2
for i in range(0, 7, 2):
    print(i)